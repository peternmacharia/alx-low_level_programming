int add(int a, int b)
{
    /**
     * add - addition function
     * @a: first integer
     * @b: second integer
     *
     * Return: the addition of the two integers
     */

    return (a + b);
}

int sub(int a, int b)
{
    /**
     * sub - subtraction function
     * @a: first integer
     * @b: second integer
     *
     * Return: the subtraction of the two integers
     */
    return (a - b);
}

int mul(int a, int b)
{
    /**
     * mul - multiplication function
     * @a: first integer
     * @b: second integer
     *
     * Return: the multiplication of the two integers
     */
    return (a * b);
}

int div(int a, int b)
{
    /**
     * div - division function
     * @a: first integer
     * @b: second integer
     *
     * Return: the division of the two integers
     */
    return (a / b);
}

int mod(int a, int b)
{
    /**
     * mod - module function
     * @a: first integer
     * @b: second integer
     *
     * Return: the module of the two integers
     */
    return (a % b);
}
